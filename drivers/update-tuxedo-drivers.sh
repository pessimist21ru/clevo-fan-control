#!/bin/bash
set -e

#==============================================================================
# Tuxedo Drivers Installer/Updater
#==============================================================================

# Проверка прав root
if [ "$EUID" -ne 0 ]; then
    echo "Пожалуйста, запустите с sudo: sudo $0"
    exit 1
fi

#==============================================================================
# Определение директорий
#==============================================================================

# Определение директории скрипта
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# Если скрипт запущен из drivers/, поднимаемся на уровень выше
if [[ "$SCRIPT_DIR" == */drivers ]]; then
    PROJECT_DIR="$(dirname "$SCRIPT_DIR")"
else
    PROJECT_DIR="$SCRIPT_DIR"
fi

# Определение домашней директории пользователя
if [ -n "$SUDO_USER" ]; then
    USER_HOME=$(getent passwd "$SUDO_USER" | cut -d: -f6)
else
    USER_HOME="$HOME"
fi

# Директория для драйверов (внутри проекта)
DRIVERS_DIR="$PROJECT_DIR/drivers"
REPO_DIR="$DRIVERS_DIR/tuxedo-drivers"
REPO_URL="https://github.com/tuxedocomputers/tuxedo-drivers.git"

echo "=== Tuxedo Drivers Installer/Updater ==="
echo ""
echo "Project directory: $PROJECT_DIR"
echo "Drivers directory: $DRIVERS_DIR"
echo ""

#==============================================================================
# Создание директории для драйверов
#==============================================================================

mkdir -p "$DRIVERS_DIR"

#==============================================================================
# Клонирование или обновление репозитория
#==============================================================================

cd "$DRIVERS_DIR"

if [ -d "$REPO_DIR" ]; then
    echo "✓ Обновление репозитория в $REPO_DIR..."
    cd "$REPO_DIR"
    if [ -n "$SUDO_USER" ]; then
        sudo -u "$SUDO_USER" git pull
    else
        git pull
    fi
else
    echo "✓ Клонирование репозитория в $REPO_DIR..."
    if [ -n "$SUDO_USER" ]; then
        sudo -u "$SUDO_USER" git clone "$REPO_URL" "$REPO_DIR"
    else
        git clone "$REPO_URL" "$REPO_DIR"
    fi
    cd "$REPO_DIR"
fi

#==============================================================================
# Сборка модулей
#==============================================================================

echo ""
echo "=== Сборка модулей ==="
make clean
make

#==============================================================================
# Установка модулей
#==============================================================================

echo ""
echo "=== Установка модулей ==="
make -C "/lib/modules/$(uname -r)/build" M="$(pwd)" modules_install

#==============================================================================
# Копирование udev правил
#==============================================================================

echo ""
echo "=== Копирование udev правил ==="
if [ -d files/usr ]; then
    cd files/usr
    find . -type f -exec install -D -m 644 {} /{} \;
    cd ../..
fi

#==============================================================================
# Создание конфигурации автозагрузки
#==============================================================================

echo ""
echo "=== Настройка автозагрузки ==="
tee /etc/modules-load.d/tuxedo-drivers.conf << 'EOF'
# Tuxedo drivers for Clevo notebooks
clevo_acpi
ite_829x
tuxedo_keyboard
tuxedo_io
tuxedo_compatibility_check
EOF

#==============================================================================
# Обновление зависимостей
#==============================================================================

echo ""
echo "=== Обновление зависимостей ==="
depmod -a

if command -v update-initramfs >/dev/null 2>&1; then
    update-initramfs -u -k all
fi

#==============================================================================
# Обновление hwdb
#==============================================================================

if command -v systemd-hwdb >/dev/null 2>&1; then
    echo "✓ Обновление hwdb..."
    systemd-hwdb update 2>/dev/null || true
fi

#==============================================================================
# Перезагрузка udev
#==============================================================================

echo "✓ Перезагрузка udev..."
udevadm control --reload-rules
udevadm trigger

#==============================================================================
# Проверка установки
#==============================================================================

echo ""
echo "=== Проверка установки ==="

# Проверка установленных модулей
MODULES_COUNT=$(find "/lib/modules/$(uname -r)" -name "*.ko*" 2>/dev/null | grep -E "clevo|tuxedo" | wc -l)
echo "Найдено модулей clevo/tuxedo: $MODULES_COUNT"

# Проверка загруженных модулей
echo ""
echo "Загруженные модули:"
lsmod | grep -E "clevo|tuxedo" || echo "  (нет загруженных модулей)"

#==============================================================================
# Вывод информации
#==============================================================================

echo ""
echo "=== Конфигурация автозагрузки ==="
cat /etc/modules-load.d/tuxedo-drivers.conf

echo ""
echo "=== Установка завершена ==="
echo "Драйверы сохранены в: $REPO_DIR"
echo ""
echo "Для загрузки модулей выполните:"
echo "  sudo modprobe clevo_acpi"
echo "  sudo modprobe tuxedo_io"
echo "  sudo modprobe tuxedo_keyboard"
echo ""
echo "Для применения изменений перезагрузитесь: reboot"